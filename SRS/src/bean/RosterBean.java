package bean;

public class RosterBean {
	private int sectionID;
	private String courseNumber;
	private String title;
	private String semester;
	private String studentID;
	private String studentFN;
	private String studentLN;
	private String studentMajor;
	private String studentGrade;
	private String instructorFN;
	private String instructorLN;

	public int getSectionID() {
		return sectionID;
	}
	public void setSectionID(int sectionID) {
		this.sectionID = sectionID;
	}
	
	public String getCourseNumber() {
		return courseNumber;
	}
	public void setCourseNumber(String courseNumber) {
		this.courseNumber = courseNumber;
	}
	
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	
	public String getSemester() {
		return semester;
	}
	public void setSemester(String semester) {
		this.semester = semester;
	}
	
	public String getStudentID() {
		return studentID;
	}
	public void setStudentID(String studentID) {
		this.studentID = studentID;
	}
	
	public String getStudentFN() {
		return studentFN;
	}
	public void setStudentFN(String studentFN) {
		this.studentFN = studentFN;
	}
	
	public String getStudentLN() {
		return studentLN;
	}
	public void setStudentLN(String studentLN) {
		this.studentLN = studentLN;
	}
	
	public String getStudentMajor() {
		return studentMajor;
	}
	public void setStudentMajor(String studentMajor) {
		this.studentMajor = studentMajor;
	}
	
	public String getStudentGrade() {
		return studentGrade;
	}
	public void setStudentGrade(String studentGrade) {
		this.studentGrade = studentGrade;
	}
	
	public String getInstructorFN() {
		return instructorFN;
	}
	public void setInstructorFN(String instructorFN) {
		this.instructorFN = instructorFN;
	}
	
	public String getInstructorLN() {
		return instructorLN;
	}
	public void setInstructorLN(String instructorLN) {
		this.instructorLN = instructorLN;
	}
}